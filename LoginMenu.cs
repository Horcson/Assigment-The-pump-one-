class LoginSys
{
    String loginID;
    String password;
    
    public void setLoginID(String loginDet)
    {
        this.loginID = loginDet;
    }

    public String getLoginID()
    {
        return loginID;
    }

    public void setPassword(String pass)
    {
        this.password = pass;
    }

    public String getPassword()
    {
        return password;
    }
}